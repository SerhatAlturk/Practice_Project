package Replits;

public class Replit157_MethodsWithReturn2Max                                                                  //Date: 17 Apr 2020 -- Time: 7:22 PM
	{
	public static int max(int x, int y)
	{
		if (x > y)
		{
			return y;
		}
		else
		{
			return x;
		}
	}

	public static void main(String[] args)
	{
		int a = 90;
		int b = 30;

		System.out.println(max(a, b));
	}

	}
